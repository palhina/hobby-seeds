/**
 * ログ関連の型定義
 *
 * このファイルには趣味の記録・ログに関する型を定義します。
 */

import type { Rating, Tag } from './common';

// ==========================================
// ログエントリー
// ==========================================

/**
 * 趣味ログのエントリー
 * ユーザーが趣味を試した際の1回分の記録
 */
export type HobbyLogEntry = {
  /** 趣味ID */
  hobbyId: number;
  /** 評価（😐 🙂 😊） */
  rating: Rating;
  /** 記録日時（ISO 8601形式） */
  loggedAt: string;
};

// ==========================================
// ログデータ
// ==========================================

/**
 * 趣味ログ全体のデータ
 * すべてのログエントリーと分析結果を含む
 */
export type HobbyLog = {
  /** ログエントリーの配列 */
  entries: HobbyLogEntry[];
  /** 😊（great）の数（ステップアップ解放判定用） */
  greatCount: number;
  /** 傾向分析結果（よく試しているタグ上位3つ） */
  topTags: Tag[];
};
